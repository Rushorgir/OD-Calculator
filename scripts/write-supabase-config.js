const fs = require('fs');
const url = process.env.SUPABASE_URL || '';
const key = process.env.SUPABASE_ANON_KEY || '';
if (!url || !key) {
  console.error('Missing SUPABASE_URL or SUPABASE_ANON_KEY');
  process.exit(1);
}
const content = `// Auto-generated by build - do not commit\nconst SUPABASE_URL = '${url}';\nconst SUPABASE_ANON_KEY = '${key}';\nconst supabaseClient = supabase.createClient(SUPABASE_URL, SUPABASE_ANON_KEY);\nif (typeof window !== 'undefined') window.supabaseClient = supabaseClient;\n`;
fs.writeFileSync('supabase-config.js', content, 'utf8');
console.log('supabase-config.js written');
